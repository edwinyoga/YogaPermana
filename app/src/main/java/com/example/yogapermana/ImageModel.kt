package com.example.yogapermana

class ImageModel {
    var nama: String? = null
    var icon: Int = 0

    fun getNamaa(): String {
        return nama.toString()
    }

    fun setNamaa(nama: String) {
        this.nama = nama
    }

    fun getIconn(): Int {
        return icon
    }

    fun setIconn(icon: Int) {
        this.icon= icon
    }

}
